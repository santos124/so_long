#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*ls;
	t_list	*tmp;

	ls = NULL;
	if (!lst)
		return (NULL);
	while (lst)
	{
		tmp = ft_lstnew(f(lst->content));
		if (!tmp)
		{
			ft_lstclear(&ls, del);
			ls = NULL;
			break ;
		}
		ft_lstadd_back(&ls, tmp);
		lst = lst->next;
	}
	return (ls);
}
